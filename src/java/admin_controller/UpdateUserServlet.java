    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String userName = request.getParameter("userName");
        String firstName = request.getParameter("firstName");
        String lastName = request.getParameter("lastName");
        String sex_raw = request.getParameter("sex");
        String phone_raw = request.getParameter("phone");
        String dob_raw = request.getParameter("dob");
        String role_raw = request.getParameter("role");

        int sex;
        int phone;
        int role;

        try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            java.util.Date utilDate = sdf.parse(dob_raw);
            java.sql.Date dob = new java.sql.Date(utilDate.getTime());
            sex = Integer.parseInt(sex_raw);
            phone = Integer.parseInt(phone_raw);
            role = Integer.parseInt(role_raw);

            User u = new User(userName, firstName, lastName, dob, sex, role, phone);
            DAO d = new DAO();
            d.updateUserAdmin(u);
            List list = d.getAll();
            request.setAttribute("list", list);
            request.getRequestDispatcher("a_user.jsp").forward(request, response);
        } catch (ParseException e) {

        }

    }

